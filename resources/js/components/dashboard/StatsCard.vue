import { computed } from 'vue'

const props = defineProps({
  title: {
    type: String,
    required: true
  },
  value: {
    type: [String, Number],
    required: true
  },
  icon: {
    type: String,
    default: 'document'
  },
  color: {
    type: String,
    default: 'indigo'
  }
})

const iconClass = computed(() => {
